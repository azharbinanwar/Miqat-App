/**
 * Single source of truth for every app color (light + dark).
 *
 * Full Material 3 token set ("Serene Devotion" scheme) PLUS our own extras
 * (success/info/warning/neutral + UI surfaces). A color is declared once per mode.
 * Access in UI via [AppTheme.colors] (e.g. `AppTheme.colors.success`).
 */
